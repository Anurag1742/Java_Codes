package If_Else_Switch_Case;

public class J02If_2 {
    public static void main(String[] args) {
        int a = 10, b = 20;

        if (a > b) {
            System.out.println("a is greater than b");
        } 

        if (a < b) {
            System.out.println("a is less than b");
        }

        if (a == b) {
            System.out.println("a is equal to b");
        }
    }
}
