package If_Else_Switch_Case;

public class J06MaxInTwo {
    public static void main(String[] args) {
        int a, b, max;

        a = 23;
        b = 14;

        if (a>b){
            max = a;
        } else {
            max = b;
        }
        System.out.println("\nMaximum : " + max);
    }
}
