package If_Else_Switch_Case;

public class J12TypeOfNumber {
    public static void main(String[] args) {
        int a = 13;

        if(a < 0){
            System.out.println("a is negative.");
        }else if(a == 0){
            System.out.println("a is zero.");
        }else{
            System.out.println("a is positive.");
        }
    }
}
