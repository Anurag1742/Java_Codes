package JAVA_Operators;

public class J06Module {
    public static void main(String[] args) {
        int x = 42;
        double y = 42.25;
        System.out.println("x mod 10 = " + x % 10); // % is a module operator
        System.out.println("y mod 10 = " + y % 10); // % is a module operator
    }
}
